String getSomatotypeHelpTextForRecommendation(String? dominantType) {
  if (dominantType == null) {
    return '';
  }

  String header = '';
  String characteristics = '';
  String training = '';
  String nutrition = '';

  switch (dominantType) {
    case 'Эктоморф':
      header = '**Доминирующий соматотип: Эктоморф**';
      characteristics = '''
      **Характеристики:**
      - Худощавое телосложение, тонкие кости, узкие плечи.
      - Ускоренный метаболизм, сложно набирать мышечную и жировую массу.
      ''';
      training = '''
      **Рекомендации по тренировкам:**
      - **Акцент:** Базовые силовые упражнения с большими весами (приседания, становая тяга, жим лежа).
      - **Интенсивность:** Высокая, с 8-12 повторениями в 3-4 подходах.
      - **Кардио:** Минимальное, чтобы не сжигать лишние калории.
      ''';
      nutrition = '''
      **Рекомендации по питанию:**
      - **Калории:** Высококалорийный рацион с профицитом энергии.
      - **Углеводы:** Повышенное потребление сложных углеводов (55-65%).
      - **Питание:** 5-7 приемов пищи в день.
      ''';
      break;
    case 'Мезоморф':
      header = '**Доминирующий соматотип: Мезоморф**';
      characteristics = '''
      **Характеристики:**
      - Атлетическое телосложение, хорошо развитая мускулатура.
      - Легко набирает мышечную массу и быстро теряет жир.
      ''';
      training = '''
      **Рекомендации по тренировкам:**
      - **Акцент:** Вариативность. Чередуйте тяжелые и легкие тренировки.
      - **Интенсивность:** Хорошо откликается на суперсеты и интервальные тренировки (HIIT).
      - **Кардио:** Умеренное количество, 2-3 раза в неделю.
      ''';
      nutrition = '''
      **Рекомендации по питанию:**
      - **Баланс:** Сбалансированный рацион (БЖУ ~30/40/30).
      - **Белок:** Достаточное количество белка для роста мышц.
      ''';
      break;
    case 'Эндоморф':
      header = '**Доминирующий соматотип: Эндоморф**';
      characteristics = '''
      **Характеристики:**
      - Округлые формы, крупное телосложение, широкие кости.
      - Замедленный метаболизм, склонность к накоплению жира.
      ''';
      training = '''
      **Рекомендации по тренировкам:**
      - **Акцент:** Кардиотренировки высокой частоты (4-5 раз в неделю).
      - **Интенсивность:** Силовые тренировки с акцентом на комплексные упражнения.
      - **Активность:** Ведите активный образ жизни.
      ''';
      nutrition = '''
      **Рекомендации по питанию:**
      - **Калории:** Умеренный дефицит калорий.
      - **Белок:** Высокое потребление белка (30-35%).
      - **Избегайте:** Простых сахаров и жирной пищи.
      ''';
      break;
    default:
      return '';
  }

  return '''

---
$header
$characteristics
$training
$nutrition
''';
}
