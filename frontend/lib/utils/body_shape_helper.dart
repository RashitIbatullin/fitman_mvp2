String getBodyShapeHelpText(String? bodyShape) {
  if (bodyShape == null ||
      bodyShape == 'Недостаточно данных' ||
      bodyShape == 'Не удалось определить') {
    return 'Нет данных для отображения справки. Введите обхваты плеч, талии и бедер, чтобы рассчитать тип фигуры.';
  }

  switch (bodyShape) {
    case 'Яблоко':
      return '''
      **Тип фигуры: Яблоко (Андроидный)**

      **Характеристики:**
      - Жировые отложения в основном концентрируются в области живота и верхней части тела.
      - Ноги и руки часто остаются стройными.
      - Талия выражена слабо.
      - Повышенный риск сердечно-сосудистых заболеваний и диабета 2-го типа из-за висцерального жира.

      **Рекомендации по тренировкам:**
      - **Акцент:** Высокоинтенсивные интервальные тренировки (HIIT) и кардио для сжигания жира (бег, плавание, быстрая ходьба).
      - **Силовые:** Комплексные упражнения на все группы мышц для ускорения метаболизма. Уделяйте внимание укреплению мышц кора и спины.
      - **Цель:** Снижение общего процента жира и уменьшение объема талии.

      **Рекомендации по питанию:**
      - **Контроль углеводов:** Ограничьте простые сахара и рафинированные углеводы. Отдавайте предпочтение сложным углеводам с низким гликемическим индексом.
      - **Белок и клетчатка:** Увеличьте потребление белка и клетчатки для чувства сытости и контроля аппетита.
      - **Жиры:** Выбирайте полезные жиры (авокадо, орехи, оливковое масло).
      ''';
    case 'Груша':
      return '''
      **Тип фигуры: Груша (Гиноидный)**

      **Характеристики:**
      - Жировые отложения скапливаются на бедрах, ягодицах и в нижней части живота.
      - Верхняя часть тела (плечи, грудь) обычно более узкая.
      - Тонкая талия.

      **Рекомендации по тренировкам:**
      - **Акцент:** Силовые тренировки на верхнюю часть тела для создания баланса (отжимания, подтягивания, упражнения на плечи).
      - **Ноги:** Многоповторные упражнения на ноги с умеренным весом для тонуса, а не для увеличения объема.
      - **Кардио:** Регулярное кардио (эллипс, велотренажер) для сжигания жира.

      **Рекомендации по питанию:**
      - **Баланс:** Сбалансированный рацион с умеренным дефицитом калорий, если цель - похудение.
      - **Избегайте:** Избытка жирной пищи.
      - **Вода:** Пейте достаточное количество воды для улучшения обмена веществ.
      ''';
    case 'Прямоугольник':
      return '''
      **Тип фигуры: Прямоугольник**

      **Характеристики:**
      - Плечи, талия и бедра примерно одной ширины.
      - Слабо выраженная талия.
      - Часто худощавое телосложение.

      **Рекомендации по тренировкам:**
      - **Акцент:** Силовые тренировки для создания изгибов. Упражнения на широчайшие мышцы спины, плечи, а также на ягодицы и бедра (приседания, выпады).
      - **Талия:** Упражнения на косые мышцы живота помогут визуально сузить талию.
      - **Цель:** Создание иллюзии более женственного силуэта "песочные часы".

      **Рекомендации по питанию:**
      - **Профицит/дефицит:** В зависимости от цели (набор массы или сушка) создавайте небольшой профицит или дефицит калорий.
      - **Белок:** Повышенное потребление белка для роста мышц.
      ''';
    case 'Перевернутый треугольник':
      return '''
      **Тип фигуры: Перевернутый треугольник**

      **Характеристики:**
      - Широкие плечи, развитая грудная клетка.
      - Узкие бедра и таз.
      - Ноги часто стройные.

      **Рекомендации по тренировкам:**
      - **Акцент:** Силовые тренировки на нижнюю часть тела для увеличения объема бедер и ягодиц (глубокие приседания, становая тяга, жим ногами).
      - **Верхняя часть тела:** Умеренные нагрузки, чтобы не увеличивать диспропорцию.
      - **Цель:** Сбалансировать пропорции верха и низа.

      **Рекомендации по питанию:**
      - **Баланс:** Сбалансированный рацион. При наборе массы акцент на силовые для ног, чтобы калории шли в "нужное" место.
      ''';
    case 'Песочные часы':
      return '''
      **Тип фигуры: Песочные часы**

      **Характеристики:**
      - Пропорциональное развитие верха и низа. Плечи и бедра примерно одной ширины.
      - Ярко выраженная тонкая талия.
      - Считается наиболее гармоничным типом фигуры.

      **Рекомендации по тренировкам:**
      - **Акцент:** Поддержание пропорций. Комплексные тренировки на все тело.
      - **Интенсивность:** Можно использовать разнообразные методы: круговые тренировки, суперсеты, классический силовой тренинг.
      - **Цель:** Поддержание мышечного тонуса и низкого процента жира.

      **Рекомендации по питанию:**
      - **Поддержание:** Питание, направленное на поддержание текущего веса и композиции тела.
      - **Баланс:** Сбалансированное потребление белков, жиров и углеводов.
      ''';
    default:
      return 'Не удалось определить тип фигуры для отображения справки.';
  }
}
